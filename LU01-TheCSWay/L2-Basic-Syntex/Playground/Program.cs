// Declaring Variables
// In C#, variables are declared with a type
int age = 25;  // Integer variable
string name = "Alice";  // String variable (note: lowercase 's' unlike Java's String)
double height = 5.8;  // Double for floating-point numbers
bool isStudent = true;  // Boolean variable

// Output variables
Console.WriteLine("Name: " + name);
Console.WriteLine("Age: " + age);
Console.WriteLine("Height: " + height);
Console.WriteLine("Is Student: " + isStudent);

// Doing Math
int a = 10;
int b = 5;
int sum = a + b;  // Addition
int difference = a - b;  // Subtraction
int product = a * b;  // Multiplication
double quotient = (double)a / b;  // Division (cast to double for floating-point result)

Console.WriteLine("Sum: " + sum);
Console.WriteLine("Difference: " + difference);
Console.WriteLine("Product: " + product);
Console.WriteLine("Quotient: " + quotient);

